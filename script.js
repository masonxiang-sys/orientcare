const languageButtons = document.querySelectorAll(".lang-button");
const translatableNodes = document.querySelectorAll("[data-i18n]");
const description = document.querySelector('meta[name="description"]');
const form = document.querySelector("#contact-form");
const formStatus = document.querySelector("#form-status");
const submitButton = form?.querySelector(".form-submit");
const year = document.querySelector("#year");

function setLanguage(language) {
  const dictionary = translations[language] || translations.es;

  document.documentElement.lang = language;
  document.title = dictionary.metaTitle;
  description.setAttribute("content", dictionary.metaDescription);

  translatableNodes.forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  if (formStatus) {
    formStatus.textContent = "";
  }

  localStorage.setItem("orientcare-language", language);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

if (form) {
  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    const language = document.documentElement.lang === "en" ? "en" : "es";
    const dictionary = translations[language];
    const originalLabel = submitButton?.textContent;

    if (formStatus) {
      formStatus.textContent = dictionary["form.submitting"];
      formStatus.classList.remove("is-error");
    }

    if (submitButton) {
      submitButton.disabled = true;
      submitButton.textContent = dictionary["form.submitting"];
    }

    try {
      const response = await fetch(form.action, {
        method: "POST",
        body: new FormData(form),
        headers: {
          Accept: "application/json",
        },
      });

      if (!response.ok) {
        throw new Error("Formspree request failed");
      }

      if (formStatus) {
        formStatus.textContent = dictionary["form.success"];
      }
      form.reset();
    } catch (error) {
      if (formStatus) {
        formStatus.textContent = dictionary["form.error"];
        formStatus.classList.add("is-error");
      }
    } finally {
      if (submitButton) {
        submitButton.disabled = false;
        submitButton.textContent = originalLabel || dictionary["form.submit"];
      }
    }
  });
}

if (year) {
  year.textContent = new Date().getFullYear();
}

const savedLanguage = localStorage.getItem("orientcare-language");
setLanguage(savedLanguage === "en" ? "en" : "es");
